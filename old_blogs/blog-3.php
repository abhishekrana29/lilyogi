      Ever wonder how the children in today's world would describe the COVID-19 pandemic to their grandchildren? While this definitely isn’t an ideal situation for the human race, aren't we all evolving in these tough times? Being confined at home for such a long period of time can be mentally challenging for adults as well as children.
