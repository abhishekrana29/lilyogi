     <p>
     I have loved to read stories since I was a child. From fantasy to reality, biographies and children's stories, cartoons to poems - you name it, and I've read it. Of course, for some time, I too took a break for reading - replaced it with studying for my CA exams (the books are way thicker than regular storybooks!). Life moved on, and my daughter Iqra was born. The center of my life shifted - from me to her. Motherhood brings out your talents, some beautiful childhood memories that you want to share with your child. I dived straight into stories for her. Stories that I remembered and loved - probably were a little complicated for her. And that's where, one night, I made up one just for her. 
<br/><br/>
Many stories later, I realized that I had developed characters that Iqra loved - I was sharing messages about life that she would ask questions on during the day. She would ask me, ' What happened then, Mumma?' I loved to make her laugh, make her think about how life is, and how our actions impact others. During the day, I would forget about my role as a storyteller and revert to being a Mom - till the time Li'l Yogis was born.
<br/><br/>
With my second baby (Li'l Yogis), the storyteller appeared in the mornings too. I had so many kids who wanted to listen and learn. After a grueling class, their minds were relaxed and absorbed like a sponge. Parents and caretakers would ask me to address a particular topic during yoga class to address some behavior/aspects of the personality of their child. I instinctively knew that there was no other way than to tell them a story about it. 
<br/><br/>
Stories are so compelling because they transport the child into another world- more malleable to their imaginations. Here they can play out different scenarios in their heads and make a distinction of what is right and wrong for them. So my request to parents is, read to your child. Even a short story which makes them laugh or one that makes them reflect on some aspect of life will stay with them. You'll shape them and make them love life more.

