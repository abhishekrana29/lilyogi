      Inspired from incredible creatures that inhabit our oceans these yoga postures are a great way to introduce children to yoga. There is something about the calm blue waters of the ocean and the wonderful animals living in it that just fascinates children. Practicing yoga helps children stay calm and fit , it reduces stress and helps them grow mentally and physically stronger. Teach your children more about ocean animals and help them connect with nature with these fun Ocean Yoga poses:
      <br /><br />
      <b> 1. Shark Pose</b><br />
      Shark pose isn't so much of a back bend as it is an exercise to lengthen the spine. It will also open the chest and lungs while stretching the shoulders and strengthening the arms, back and core muscles.
      <br /><br />
      <b> How to Do </b>
      <br />
      1. Begin by lying down on your belly.
      <br />
      2. Clasp your hands behind your back to make a shark fin.
      <br />
      3. Breathe in and lift your arms, leg and chest all at the same time. Keep your neck long and eyes on a focus point in front of you.
      <br />
      4. Slowly bring your chest, legs and arms back down to the floor and relax.

      <br /><br />

      Benefits
      <br />
      - Strengthen
      <br />
      - Stretch
      <br /><br />

      <b> 2. Crab Pose/Ardha Purvottanasana</b>
      <br />
      Time to get some movement with the most famous pinchers of the land and sea- crabs!
      <br /><b> How to Do</b><br />

      1. Start by sitting on your bottom with your knees bent, feet flat on the floor.<br />
      2. Place your hands behind you on the floor, palms flat and fingers pointing towards your body.<br />
      3. Lift your belly to the sky. Try crab walking forward, backward and side to side.<br />
      <br /><br />

      Benefits:
      - Builds coordination and balance <br />
      - Strengthens the core<br />
      - Gross motor development<br />

