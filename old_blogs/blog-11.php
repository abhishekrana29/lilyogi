These mats though cheap, are not suitable for health, and for a long time, I have been researching their impact on health and the environment. I found the following facts about PVC mats: 
    <br/><br/>
Non- Recyclable: We are in 'Love-Our-Planet-Mode' and guess what? These mats cannot be reused and recycled! PVC is "resistant to acid, alkali, and almost all inorganic chemicals. Although PVC swells or dissolves in aromatic hydrocarbons, ketones, and cyclic ethers, PVC is hard to dissolve in other organic solvents." It takes about 1000 years to decompose.
<br/><br/>
Health Hazard: PVC mats use something called Phthalates to make the mats flexible. If used in a warm environment, it leaches from the mats and clings to dust particles that you may inhale. Phthalates are linked to reproductive health issues, congenital disabilities, fertility issues, obesity, and cancer.
<br/><br/>
Environment's Enemy: During the manufacturing of PVC mats, dioxin is released into the atmosphere, which is a known carcinogen. 
<br/><br/>
In India, we have a tradition of using cotton and reed mats. Moving towards a Fit India, we must look at the things around us that may impact our overall health. Mats first, please!
