<br/><br/>
2) <b> Mat Placement</b>: While positioning on the floor, use your HANDS ONLY!
<br/><br/>
3) <b>Your Mat is ONLY for Yoga</b>: Don't use your Yoga mat for other activities. The energies that you generate during your yoga asanas magnetize your mats.
<br/><br/>
4)  <b>Be practical</b>: If your palms and soles are the sweaty types, carry a towel to wipe them down in between the postures. Also, air-dry your cotton mats to ensure that no moisture is on it before your next yoga session!
