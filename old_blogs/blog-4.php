     <p>
     The awareness of breath and practicing breathing techniques or exercises is called Pranayama. Yoga Breaths strengthen the connection between mind and body, helping it to relax and rejuvenate. Practicing yoga breaths with your child will help you and your child stay calm and relaxed and release stress or anxiety.
        Listed  below are 5 fun Yoga Breaths that you can practice  with your child:
      <br /><br/>
    <b> Bumble Bee Breath</b>This breath is presented in three steps, working to bring awareness from the outside in. After each step encourage your child to share his experience.
   <br><b>Benefits</b><br>
     Calm <br>
     Restore <br/>
   <h5 class="pt-4"><b>What to Do</b></h5>
   1. Sit or stand up tall.<br>
   2. Take a slow, deep breath in through your nosebefore exhaling out to “Hummmmmmm” for as long as possible.<br>
   3. Try it again but this time with your eyes closed. Focus on the humming sound. What did you notice? Was it different with your eyes closed?<br>
   4. Try it again with your eyes closed and ears covered with your hand. Where did your focus go? What did you notice? How do you feel?<br>
   5. Experiment with breathing out other sounds such as “Zzzzz”, “Ohhhh”, “Shhhh”, “Lllll” or “Sssss”.
    <br /><br /> </p>

   <h5><b>Rest with Breathing Buddies</b></h5> <br/>

In this activity the weight of the breathing buddy (a stuffed beanie doll or animal) brings your child’s awareness to her breath as he/she feels and sees the buddy moving u and down on their abdomen.
<br/><br/>
<h5><b>Benefits</b></h5>
Calm <br>
Restore <br/>

<h5 class="pt-4 pb-4"><b>What to Do</b></h5>

1. Lie down on you back. Place your buddy on your belly so that it can get comfortable for a ride, rising and falling as u breath in and out slowly and deeply. <br>2. Close your eyes and start to practice your balloon breathing, by breathing in, filling your belly all the way up, and then slowly exhaling to let out all the air.<br>3. Feel you buddy moving up and down
