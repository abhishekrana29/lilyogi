    <br/><br/>
<h6><b>Seated side bends</b></h6> 
<br/>
<h6><b>How to do:</b></h6> 
<br/>
1. Start seated with legs crossed. 
<br/><br/>
2.  Sit tall with a straight spine
<br/><br/>
3. Lift your left arm up,bring your right hand off to the right, then side bend to the right. 
<br/><br/>
4. Take a few breaths before repeating on the left.
<br/><br/>
<h6><b>Seated single leg side bend</b></h6> 
<br/>
<h6><b>How to do:</b></h6>
<br/>
1. Begin by extending the right leg to the side and bend the left knee.
<br/><br/>
2. Place the right palm on top of the right shin or if possible touch your ankle/toes.
<br/><br/>
3. Inhale and lengthen the spine, lift the left arm up towards the sky and then start to extend the left arm to the side to create a side range stretch. 
<br/><br/>
4. Look up at your left arm, push the sitting bone down and allow the left hip to open.
<br/><br/>
<h6><b>Benefits:</b></h6>
<br/>
1. Increases the flexibility of the spine.
<br/><br/>
2. Energises and invigorates the mind.
<br/><br/>
3. Helps to alleviate lower back pain.
<br/><br/>
4. Stretches the intercostal muscle intern increasing the lung expansion capabilties.
   
