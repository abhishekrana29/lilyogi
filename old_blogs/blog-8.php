<b>Physical benefits of yoga</b>
<br><br>
Full body exercise - Yoga aims at the whole body fitness rather than targeting one body part at a time. Kids who are born natural yogis.By practising yoga they are able to retain their flexibility which is mostly lost with age.
<br><br>
Yoga teaches body and sensory awareness  - Yoga asanas helps kids understand how different body parts work in sync along with their breath to get the whole system together. The awareness created in turn helps them respect, love and take care of their body
<br><br>
Increases immunity - A Childs immune system is of utmost importance. The initial years are the building blocks to a healthy future. Yoga is a natural booster which improves resistance and helps build a strong defence mechanism which in turn improves the body’s immunity.
<br><br>
Keeps the heart, respiratory and digestive system fit - Yoga focuses on ones breath which makes sure there is a good circulation of oxygen and nutrient rich blood to the heart. Stomach pain due to flatulence and constipation are common issues in young kids. Yoga helps improves the disguise system and combat these issues.
<br><br>
Improves posture - The sedentary lifestyle led by todays kids leads to many health issues including bad posture. Most children are glued to the screen, making them overweight and obese. Yoga is one of the correct ways to improve ones posture.
