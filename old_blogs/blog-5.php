<br/><br/>
Yoga has been used for centuries for the holistic development of mind and body.
<br/><br/>
Here are five simple poses that you can practise with your family members or children at home.
     <br/><br/>
    <h5 class="pt-4 pb-4"><b>1. Adho Mukha Svanasana/ Downdog</b></h5> 
    This pose helps in calming the mind and stretching the body. It helps to strengthen the upper body while also providing an opportunity to stretch the shoulders, arms and legs.  <br/> <br/>
    
    <h5 class="pt-4 pb-4"><b>How to Do</b></h5>
    <ul>
        <li>Come onto your hand and knees with the top of your feet flat on the floor.</li>
<li>Tuck your toes under and push your hips up and back into the air. Keep your arms straight, your head and neck relaxed away from your shoulders.</li>
<li>Press your hips back as you stretch your heels down toward the floor.</li>
<li>Bring your lower back down to the start pose and sit back on your heels.</li>
    
    </ul>

    <h5 class="pt-4 pb-4"><b>2. Badakonasana/Butterfly</b></h5>
    Butterfly can help open the hips and stretch the inner thighs. This pose helps in opening up tight hips and increases blood flow in the pelvic region.
<br/> <br/>
If your child has tight hips, have her sit on a folded blanket so that her hips are above her legs and feet.
<br/> <br/>
For the most benefit, encourage your child to sit up tall while practicing butterfly pose. 
<br/> <br/>

    <h5 class="pt-4 pb-4"><b>How to Do</b></h5>

    <ul>
        <li>Sit up tall and bring the bottoms of your feet together to form your butterfly wings.</li>
<li>Breathing in lift your knees. Breathing out lower your knees</li>
<li>Continue to breathe and flap your wings.</li>
        </ul>

        <h5 class="pt-4 pb-4"><b>3. Vrishkasana/ Tree Pose</b></h5>
Focus, concentration and balance can be difficult for some children.
<br/> <br/>
With practice, Tree Pose offers an opportunity to enhance those skills and builds confidence. It helps in improving focus and connects the mind, body and soul.

        <h5 class="pt-4 pb-4"><b>How to Do</b></h5>

        <ul>
        <li>Begin with standing straight with toes, feet and knees together. Palms together at the sternum. Find a focus point with your eyes.</li>
        <li>When you are ready shift your weight to the left foot and turn your right knee out to the side.</li>
    <li>Rest your right heel on the left ankle, keeping your toes on the floor. If you are feeling balanced, try lifting your right foot up to rest on the inside of your calf, or bring your foot up all the way to press into your inner thigh.</li>
       <li>Slowly grow your arm branches by reaching up to the sky. You can bring your hands together over head or keep them apart.</li>
    <li>Balance here for three to ten slow, deep breaths or for as long as you are able to.</li>
        </ul>
        <h5 class="pt-4 pb-4"><b>4. Bhujangasana/Cobra Pose</b></h5>
        
        A heart opening pose which also works on strengthening the back, arm, shoulders and wrists.

        <h5 class="pt-4 pb-4"><b>How to Do</b></h5>
        <ul>
            <li>Begin by lying down on your belly and palms by your chest and elbows tucked in close to your sides..</li>
            <li>Slowly lift the chest up and forward coming into a backbend.</li>
            <li>Looking up hold this pose fro 3-5 breaths.</li>
            <li>Slowly bring your forehead and chest back to the floor and relax.</li>
        
            </ul>

            <h5 class="pt-4 pb-4"><b>5. Yashtikasana/Stick pose</b></h5>
            Also known as the Stick Pose because it resembles a stick lying straight on the floor.
            <br/><br/>
            It helps in full body stretch and also aids in increasing height among children.

            <h5 class="pt-4 pb-4"><b>How to Do</b></h5>

            <ul>
                <li>Lie on your back, with legs fully extended, feet together and the hands beside the body.</li>
                <li>Inhaling, raise both the hands in a semi circular arc, keeping the hands fully stretched and parallel to each other , to take them above the head.</li>
                <li>Simultaneously stretch the toes outwards.</li>
                <li>Synchronizing the above yo steps, maintain this fully stretched position for 6 seconds, retaining the breath.</li>
                <li>Exhaling, relax the body and toes, bring the hands back close to the body, to complete 1 round.</li>
                <li>Practice 4 rounds/per session.</li>
            </ul>
