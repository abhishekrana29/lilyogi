<h5 class="pt-4"><b>Back to Back Breathing</b></h5><br>
Back to Back Breathing is a partner breath exercise that offers an opportunity to connect with your child in a deep and meaningful way. Use it as transition to bedtime, sharing time, or anytime you need calm and connection.
<br/> <br/>
<h5><b>Benefits</b></h5>
Calm <br>
Connect <br>
Focus <br/>

<h5 class="pt-4 pb-4"><b>What to Do</b></h5>

1. Sit back to back with your partner in Easy pose and close your eyes. let the weight of your body push back against your partner.<br>
2. Place your hands slowly on your lower belly.<br>
3. imagine your belly is a blood. To inflate your balloon belly, breathe in slowly though you nose, filling up your balloon belly. stick out your belly to fill it up all the way.<br>
4. Slowly exhale through your nose and feel your ballon belly deflate.<br>
5.Continue breathing together for 1 minute or more with your partner.<br>
6. What did you notice? How do you feel? Share with your partner.<br>


<h5 class="pt-4 pb-4"><b>Take 5 Breathing Exercise</b></h5>


This Take 5 Breathing Exercise is a wonderful way to switch off the stress response. It brings the body back into balance, slowing and deepening the breath and slowing down the heart rate, while harnessing the need to move with an action that requires focus and provides sensory feedback to our brains. Best of all, Take 5 Breathing is simple, enjoyable, free, and you can use it anywhere, anytime.

<h5 class="pt-4 pb-4"><b>Benefits</b></h5>
Calm <br>
Focus <br/>

<h5 class="pt-4 pb-4"><b>What to Do</b></h5>

Stretch your hand out like a star. <br>
Get the pointer finger of the other hand ready to trace your fingers up and down <br>
Slide up each finger slowly slide down the other side. <br>
Breath in through your nose and breath out through your mouth. <br>
As you breath in slide up and as you breath out slide down. <br> 
Keep going until you have finished tracing your hand <br>

<h1 class="pt-4 pb-4"><b>Woodchopper Breath</b></h1>
Wood Chopper Breath  is an excellent tool for students to “huh” it out it a controlled and safe manner. Use woodchopper to emotionally prepare. Taking in the positive and releasing the negative is the first step in being able to develop a growth mindset. 
<b>Benefits</b>
Releases stress Energises
<h5 class="pt-4 pb-4"><b>What to Do</b></h5>

1.Stand tall in Mountain Pose and take a few deep breaths. <br>
2.Then take your feet mat-width apart (a little wider than hip-width). Clasp your hands together in front of your body. Take a long breath in while raising your hands above your head. <br>
3.Then, on a vigorous exhale with your mouth open, forcefully take your hands down between your legs. Hang your head and completely let go of all the tension in your body. <br>
4.Close your eyes, if that feels comfortable, and repeat the steps. Let the children feel the effects of this energizing breathing technique.
    
